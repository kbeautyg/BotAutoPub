async def channel_id_filter(message):
    """–§–∏–ª—å—Ç—Ä –¥–ª—è –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Å–æ–æ–±—â–µ–Ω–∏–µ ID –∫–∞–Ω–∞–ª–∞"""
    if not message.text:
        return False
    text = message.text.strip()
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –ø–æ—Ö–æ–∂–µ –ª–∏ –Ω–∞ ID –∫–∞–Ω–∞–ª–∞ –∏–ª–∏ username
    return text.startswith('@') or (text.startswith('-') and text[1:].isdigit())

